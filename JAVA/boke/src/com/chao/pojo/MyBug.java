package com.chao.pojo;

public class MyBug {
	
	private Integer bugId;
	private String bugError;
	private String bugReason;
	private String bugSolve;
	

	public Integer getBugId() {
		return bugId;
	}
	public void setBugId(Integer bugId) {
		this.bugId = bugId;
	}
	public String getBugError() {
		return bugError;
	}
	public void setBugError(String bugError) {
		this.bugError = bugError;
	}
	public String getBugReason() {
		return bugReason;
	}
	public void setBugReason(String bugReason) {
		this.bugReason = bugReason;
	}
	public String getBugSolve() {
		return bugSolve;
	}
	public void setBugSolve(String bugSolve) {
		this.bugSolve = bugSolve;
	}
	
	
	
}
